#Calcula el área de un triángulo

base= 0 
altura=0

print ("\t Cálculo de área de un triángulo")

print ("Ingrese la base del triángulo: ")
base= float(input())

print("\n Ingrese la altura del triángulo")
altura= float(input())

area=(base*altura)/2

print(f"El área del triángulo igual a: {area}")