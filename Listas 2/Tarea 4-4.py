""" 4 - Crea una lista (de lo que quieras) con al menos 8 elementos y usando slicing haz lo siguiente:
    * Imprime los primeros 3 elementos de la lista
    * Imprime 3 elementos de la mitad
    * Imprime los ultimos 3 elementos """

